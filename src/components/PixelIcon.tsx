export default function PixelIcon({ name, size = 32, color = "#00e5ff" }: PixelIconProps) {
  const grid = icons[name];
  if (!grid) return null;

  const pixelSize = size / 8;

  return (
    <svg width={size} height={size} viewBox={`0 0 ${size} ${size}`}>
      {grid.map((row, y) =>
        row.map((cell, x) =>
          cell ? (
            <rect
              key={`${x}-${y}`}
              x={x * pixelSize}
              y={y * pixelSize}
              width={pixelSize}
              height={pixelSize}
              fill={color}
            />
          ) : null
        )
      )}
    </svg>
  );
}
