// Mood to destination matching algorithm
export function getDestinationsByMood(moodId: string): Destination[] {
  // Primary matches: destinations that have this mood
  const primary = destinations.filter((d) => d.moods.includes(moodId));

  // Sort by how prominently the mood features (earlier in moods array = more relevant)
  primary.sort((a, b) => {
    const aIndex = a.moods.indexOf(moodId);
    const bIndex = b.moods.indexOf(moodId);
    return aIndex - bIndex;
  });

  return primary;
}

export function getRandomDestination(): Destination {
  return destinations[Math.floor(Math.random() * destinations.length)];
}

export function getDestinationBySlug(slug: string): Destination | undefined {
  return destinations.find((d) => d.slug === slug);
}

